package no.taardal.pixelcave.domain.animation;

public class SpriteSheetCoordinate {

    private int frames;
    private int x;
    private int y;

    public int getFrames() {
        return frames;
    }

    public void setFrames(int frames) {
        this.frames = frames;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
